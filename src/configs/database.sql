-- Tabla cursos
CREATE TABLE cursos (
	id SERIAL PRIMARY KEY,
	nombre VARCHAR(50) NOT NULL,
	descripcion VARCHAR(100),
	fecha_creacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	fecha_actualizacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabla alumnos
CREATE TABLE alumnos (
	id SERIAL PRIMARY KEY,
	nombre VARCHAR(50) NOT NULL,
	apellido VARCHAR(50) NOT NULL,
	id_curso INTEGER REFERENCES cursos(id),
	fecha_nacimiento DATE,
	hace_deportes BOOLEAN DEFAULT FALSE,
	fecha_creacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	fecha_actualizacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabla grupos_pdp
CREATE TABLE grupos_pdp (
	id SERIAL PRIMARY KEY,
	nombre VARCHAR(50) NOT NULL,
	descripcion VARCHAR(100),
	fecha_creacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	fecha_actualizacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabla intermedia alumnos_grupos_pdp
CREATE TABLE alumnos_grupos_pdp (
	id_alumno INTEGER REFERENCES alumnos(id),
	id_grupo_pdp INTEGER REFERENCES grupos_pdp(id),
	PRIMARY KEY (id_alumno, id_grupo_pdp),
	fecha_creacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	fecha_actualizacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

